static void init_PWM  (void)
{
    PWM = 0;
    PWM &= ~(1<<0); //1 OFF  0 ON
    PWM |= 50<<3; //50%

    PWM |= 37<<10;

    // f = 30M/DIV
    // DIV = 30M/f
    // f =   1/T = 1/2ms = 500Hz
    // DIV = 30M/500 = 60000
    PWM |= 60000<<15;
}

void Inicializar( void )
{

	Inicializar_Sensor( );
	init_PWM();
    return ;
}

